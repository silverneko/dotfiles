doge_cat() {
  local DOGE=(
    "           ▄              ▄      "
    "          ▌▒█           ▄▀▒▌     "
    "          ▌▒▒█        ▄▀▒▒▒▐     "
    "         ▐▄▀▒▒▀▀▀▀▄▄▄▀▒▒▒▒▒▐     "
    "       ▄▄▀▒░▒▒▒▒▒▒▒▒▒█▒▒▄█▒▐     "
    "     ▄▀▒▒▒░░░▒▒▒░░░▒▒▒▀██▀▒▌     "
    "    ▐▒▒▒▄▄▒▒▒▒░░░▒▒▒▒▒▒▒▀▄▒▒▌    "
    "    ▌░░▌█▀▒▒▒▒▒▄▀█▄▒▒▒▒▒▒▒█▒▐    "
    "   ▐░░░▒▒▒▒▒▒▒▒▌██▀▒▒░░░▒▒▒▀▄▌   "
    "   ▌░▒▄██▄▒▒▒▒▒▒▒▒▒░░░░░░▒▒▒▒▌   "
    "  ▌▒▀▐▄█▄█▌▄░▀▒▒░░░░░░░░░░▒▒▒▐   "
    "  ▐▒▒▐▀▐▀▒░▄▄▒▄▒▒▒▒▒▒░▒░▒░▒▒▒▒▌  "
    "  ▐▒▒▒▀▀▄▄▒▒▒▄▒▒▒▒▒▒▒▒░▒░▒░▒▒▐   "
    "   ▌▒▒▒▒▒▒▀▀▀▒▒▒▒▒▒░▒░▒░▒░▒▒▒▌   "
    "   ▐▒▒▒▒▒▒▒▒▒▒▒▒▒▒░▒░▒░▒▒▄▒▒▐    "
    "    ▀▄▒▒▒▒▒▒▒▒▒▒▒░▒░▒░▒▄▒▒▒▒▌    "
    "      ▀▄▒▒▒▒▒▒▒▒▒▒▄▄▄▀▒▒▒▒▄▀     "
    "        ▀▄▄▄▄▄▄▀▀▀▒▒▒▒▒▄▄▀       "
    "           ▒▒▒▒▒▒▒▒▒▒▀▀          "
    "                                 "
  )
  local eof=false
  local line
  while ! ${eof} && IFS= read -r line; do
    for doge_line in "${DOGE[@]}"; do
      echo -ne "\e[7m${doge_line}\e[0m "
      if ! ${eof}; then
        echo "${line}"
        if ! IFS= read -r line; then
          eof=true
        fi
      else
        echo ""
      fi
    done
  done
}
